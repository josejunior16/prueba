    private void jtfgarantiaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfgarantiaKeyTyped
       char letra=evt.getKeyChar();

        if(letra<'0' || letra>'9'){
            
            evt.consume();
            
        }
       garantia=garantia+letra;
        gar=Integer.parseInt(garantia);
        if(gar>24){
            JOptionPane.showMessageDialog(null,"Numero invalido, la garantia no excede a 24 meses");
            jtfgarantia.setText(null);
            evt.consume();
            gar=0;
            garantia="";
        }
        
       
         int limite=2;
        if(jtfgarantia.getText().length()==limite){
            evt.consume();
        }
    }//GEN-LAST:event_jtfgarantiaKeyTyped

    private void jtfprecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfprecioKeyTyped
        char letra=evt.getKeyChar();
         
         precio=precio+letra;
        pre=Double.parseDouble(precio);
         if((letra<'0' || letra>'9') && letra!='.'){
            evt.consume();

        }
         int limite=6;
        if(jtfprecio.getText().length()==limite){
            evt.consume();
        }
        
        if(pre<0.01 || pre>999.99){
            JOptionPane.showMessageDialog(null,"Valor fuera de rango \n. Rango (mayor a 0.01 y menor o igual a 999.99 )");
            jtfprecio.setText(null);
            evt.consume();
            pre=0;
            precio="";
        }    
    }//GEN-LAST:event_jtfprecioKeyTyped

