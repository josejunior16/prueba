    private void jtfnumKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfnumKeyTyped
     char letra= evt.getKeyChar();
     
       cpu=cpu +letra;
       dato=Integer.parseInt(cpu);
       if(dato>12){
           JOptionPane.showMessageDialog(null, "el rango de ingreso debe ser menor o igual a 12");
           jtfnum.setText(null);
           evt.consume();
           dato=0;
           cpu="";
       }
       if(letra<'0' || letra>'9'){
           evt.consume();
       }
       
        int limite=2;
        if(jtfnum.getText().length()==limite){
            evt.consume();
        }
        
    }//GEN-LAST:event_jtfnumKeyTyped

    private void jtfpantallaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfpantallaKeyTyped
       char letra=evt.getKeyChar();
         
         pantalla=pantalla+letra;
        pant=Double.parseDouble(pantalla);
        
        if(pant<3.0 || pant>6.7){
            JOptionPane.showMessageDialog(null,"NO se permiten valores mayores a 6.7 ni menores a 3.0");
            jtfpantalla.setText(null);
            evt.consume();
            pant=0;
            pantalla="";
        }
         
         if((letra<'0' || letra>'9') && letra!='.'){
            evt.consume();

        }
         int limite=3;
        if(jtfpantalla.getText().length()==limite){
            evt.consume();
            
        }
    }//GEN-LAST:event_jtfpantallaKeyTyped

